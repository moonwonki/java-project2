package org.example.week3.day1;

public class ForReverse {
    public static void main(String[] args) {
        for (int i = 10; i >= 0 ; i--){
            System.out.println(i);
        }

        for (int i = 10; i > 0; i--){
            System.out.println(i);
        }

        /*
        for (int i = 10; i > 0; i++){
            System.out.println(i);
        }
         */
    }
}
