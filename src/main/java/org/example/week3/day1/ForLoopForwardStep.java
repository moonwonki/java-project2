package org.example.week3.day1;

public class ForLoopForwardStep {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i += 2){
            System.out.println(i);
        }
        System.out.println("===========================");
        for (int i = 1; i < 10; i+= 2){
            System.out.println(i);
        }
    }
}
