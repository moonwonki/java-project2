package org.example.week2.day2;

public class StringSplit {
    public static void main(String[] args) {
        String strNotSplit = "10 3 19 28 7 488";
        String strArr[] = strNotSplit.split(" ");

        System.out.println("strArr[0] = " + strArr[0]);
        System.out.println("strArr[0] = " + strArr[1]);
        System.out.println("strArr[0] = " + strArr[2]);
        System.out.println("strArr[0] = " + strArr[3]);
        System.out.println("strArr[0] = " + strArr[4]);
        System.out.println("strArr[0] = " + strArr[5]);
    }
}
