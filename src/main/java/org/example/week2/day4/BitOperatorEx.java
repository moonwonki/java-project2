package org.example.week2.day4;

public class BitOperatorEx {
    public static void main(String[] args) {
        int iVal1 = 1;

        System.out.println(iVal1 << 1);
        System.out.println(iVal1 << 2);

        int iVal2 = 32;

        System.out.println(iVal2 >> 1);
        System.out.println(iVal2 >> 2);
    }
}
