package org.example.week2.day4;

public class ComparisonOperatorsEx {
    public static void main(String[] args) {
        int iVal1 = 10;
        int iVal2 = 20;

        System.out.printf("iVal1 == iVal2 = %b", iVal1 == iVal2);
        System.out.printf("iVal1 != iVal2 = %b", iVal1 != iVal2);
        System.out.printf("iVal1 < iVal2 = %b", iVal1 < iVal2);
        System.out.printf("iVal1 <= iVal2 = %b", iVal1 <= iVal2);
        System.out.printf("iVal1 > iVal2 = %b", iVal1 > iVal2);
        System.out.printf("iVal1 >= iVal2 = %b", iVal1 >= iVal2);
    }
}
