package org.example.week2.day1;

public class DivideIntoTwo {
    public static void main(String[] args) {
        //1 + 2 / 1
        float floatOne = 1;
        float floatTwo = 2;

        System.out.printf("%f\n", floatOne + floatOne / floatTwo);

        int intOne = 1;
        int intTwo = 2;

        System.out.printf("%d\n", intOne + intOne / intTwo);

        System.out.printf("%d\n", 9 / 2);
        System.out.printf("%f\n", 9 / 2.0);
    }
}
