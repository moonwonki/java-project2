package org.example.week2.day1;

public class FloatLength {
    public static void main(String[] args) {
        float f1 = 1.12345678f;
        double d1 = 1.1234567890123456789012345678901234567890;
        System.out.println(f1); // 1.1234568
        System.out.println(d1); // 1.1234567890123457
    }
}
