package org.example.week2.day3;

public class ArithmeticOperators2 {
    public static void main(String[] args) {
        int aNumber = 10;
        int bNumber = 3;
        System.out.println(10 + bNumber);
        System.out.println(aNumber - 3);
        System.out.println(aNumber * bNumber);
        System.out.println(10 * 3 / bNumber);
        System.out.println(49 % bNumber);
    }
}

