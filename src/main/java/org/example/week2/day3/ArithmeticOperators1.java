package org.example.week2.day3;

public class ArithmeticOperators1 {
    public static void main(String[] args) {
        System.out.printf("a + b = %d\n", 3 + 10);
        System.out.printf("a - b = %d\n", 3 - 10);
        System.out.printf("a * b = %d\n", 3 * 10);
        System.out.printf("a / b = %d\n", 3 / 10);
        System.out.printf("b %% a = %d\n", 10 % 3);
    }

}
